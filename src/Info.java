class Info {
    double L;
    double B;
    double Ve;
    double Vn;
    double sigVe;
    double sigVn;
    double sigVen;
    String ID;
    Info (double L, double B, double Ve, double Vn, double sigVe, double sigVn,double sigVen,String ID) {
        this.L = L;
        this.B = B;
        this.Ve = Ve;
        this.Vn = Vn;
        this.sigVe = sigVe;
        this.sigVn = sigVn;
        this.sigVen = sigVen;
        this.ID = ID;
    }
}